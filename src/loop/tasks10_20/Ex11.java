package loop.tasks10_20;

public class Ex11 {

    public static void main(String[] args) {



        for (int i =1;i<3;i++){
            for (int j = 0;j<i;j++){
                System.out.print("*");
            }
            System.out.println();

        }

        System.out.println();

        for (int i =1;i<=3;i++){
            for (int j =0; j<i;j++ ){
                System.out.print("*");
            }
            System.out.println();
        }

        System.out.println();

        for (int i =1;i<6;i++){
            for (int j = 0;j<i;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
